    public Timer timer;
    
     public void screenRefresh() {
        timer = new Timer(200, new ActionListener() {  //1000 1 sec
            @Override
            public void actionPerformed(ActionEvent e) {
                refreshVentilacija();
            }
        });
    }
     
     private void refreshVentilacija() {
        img.getStanjeVentilacije(jLabelUbacivanjeSvezegVazduha, ventilacija.getUbacivanjeVazduha().getPozicija1(), ventilacija.getUbacivanjeVazduha().getPozicija2());
        img.getStanjeVentilacije(jLabelIzvlacenjeVazduha, ventilacija.getIzbacivanjeVazduha().getPozicija1(), ventilacija.getIzbacivanjeVazduha().getPozicija2());
    }
