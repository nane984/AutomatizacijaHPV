    private void startScreenRefresh() {
        timer = new Timer(100, new ActionListener() {  //1000 1 sec
            @Override
            public void actionPerformed(ActionEvent e) {
                int a = ventil.getVrednosti().getPolozajVrednost();
                jLabelProcenat.setText(ShiftPoint.shiftonePoint(a, ventil.getBrDecimala()) + " %");
                jProgressBar1.setMinimum(protok.getProtok().getMin());
                jProgressBar1.setMaximum(protok.getProtok().getMax());
                jProgressBar1.setValue(protok.getProtok().getVrednostRegistra());
                jLabelProtok.setText(Integer.toString(protok.getProtok().getVrednostRegistra()) + " m3/h");
                jLabelMin.setText(Integer.toString(protok.getProtok().getMin()));
                jLabelMax.setText(Integer.toString(protok.getProtok().getMax()));
                openCloseStop(ventil.getVrednosti().getOtvoren(), ventil.getPcOtvori(), ventil.getVrednosti().getZatvoren(), ventil.getPcZatvori());
                disposeTimer();
            }
        });
    }

    private void openCloseStop(boolean otvoren,int adresaOtvaranje, boolean zatvoren, int adresaZatvaranje) {
        
        if (otvoren && brPokusaja==0) {
            brPokusaja ++;
            openClose(false, adresaOtvaranje);
        }
        if(zatvoren && brPokusaja==0){
            brPokusaja ++;
            openClose(false, adresaZatvaranje);
        }
        if(!otvoren && !zatvoren){
            brPokusaja = 0;
        }
        
    }

    private void openClose(boolean open, int adr) {
        StartApp.comm.offset = adr;
        StartApp.comm.writeDigital = true;
        StartApp.comm.mxValue = open;
    }
    
    private void stop(int adr){
        StartApp.comm.offset = adr;
        StartApp.comm.writeDigitals = true;
        boolean[] vr = {false, false};
        StartApp.comm.mxValues = vr;
    }
    
    public void startTimer(boolean start){
        if(start){
            timer.start();
        }else{
            timer.stop();
        }
    }
    
    public void disposeTimer(){
        if(!this.isShowing()){
            timer.stop();
        }
    }
