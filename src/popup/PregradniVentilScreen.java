    public void startScreenRefresh() {
        timer = new Timer(100, new ActionListener() {  //1000 1 sec
            @Override
            public void actionPerformed(ActionEvent e) {
                jToggleButtonOtvori.setEnabled(ventil.getVrednostiReg().getEnableOtvori());
                jToggleButtonZatvori.setEnabled(ventil.getVrednostiReg().getEnableZatvori());
                img.setImgHorizontalno(jLabelVentil, ventil);
                disposeTimer();
            }
        });
    }
    
    public void startTimer(boolean start){
        if(start){
            timer.start();
        }else{
            timer.stop();
        }
    }  
    
    public void disposeTimer(){
        if(!this.isShowing()){
            timer.stop();
        }
    }
    
    private void selected() {
        if (ventil.getVrednostiReg().isPolozajVirtualno()) {
            jRadioButtonSoftver.setSelected(true);
            jRadioButtonSenzori.setSelected(false);
        } else {
            jRadioButtonSoftver.setSelected(false);
            jRadioButtonSenzori.setSelected(true);
        }
    }

