    private void fillTable() {
        List<db.Pregventodrzavanje> proba = pregVentOdrzavanjeService.getVentil(ventil.getText());
        int i = 0;
        Object[][] obj = new Object[proba.size()][3];
        for (db.Pregventodrzavanje auto : proba) {
            obj[i][0] = i + 1;
            obj[i][1] = auto.getOpis();
            obj[i][2] = Convert.convertDateToStringDate(auto.getDatum());
            i++;
        }

        DefaultTableModel model = new DefaultTableModel(obj, new Object[]{"r.b.", "Opis", "Datum"});
        jTable1.setModel(model);
        jTable1.setRowMargin(4);
        jTable1.setRowHeight(40);
        jTable1.setShowGrid(true);
        jTable1.getColumnModel().getColumn(0).setMaxWidth(45);
        jTable1.setDefaultEditor(Object.class, null);

    }


