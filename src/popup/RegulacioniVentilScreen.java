    private  Timer timer;
    
    private void startScreenRefresh() {
        timer = new Timer(100, new ActionListener() {  //1000 1 sec
            @Override
            public void actionPerformed(ActionEvent e) {
                int a = ventil.getVrednosti().getPolozajVrednost();
                jLabelProcenat.setText(ShiftPoint.shiftonePoint(a,ventil.getBrDecimala()) + " %");
                jProgressBar1.setMinimum(protok.getProtok().getMin());
                jProgressBar1.setMaximum(protok.getProtok().getMax());
                jProgressBar1.setValue(protok.getProtok().getVrednostRegistra());
                jLabelProtok.setText(Integer.toString(protok.getProtok().getVrednostRegistra()) + " m3/h");
                jLabelMin.setText(Integer.toString(protok.getProtok().getMin()));
                jLabelMax.setText(Integer.toString(protok.getProtok().getMax()));
                disposeTimer();
            }
        });
    }
    
    public void startTimer(boolean start){
        if(start){
            timer.start();
        }else{
            timer.stop();
        }
    }
    
    public void disposeTimer(){
        if(!this.isShowing()){
            timer.stop();
        }
    }
