public class Extended {
    private boolean lokalno;
    private boolean daljinski;
    private boolean ukljucen;
    private boolean enableOn;
    private boolean enableOff;

    public boolean isLokalno() {
        return lokalno;
    }

    public void setLokalno(boolean lokalno) {
        this.lokalno = lokalno;
    }

    public boolean isDaljinski() {
        return daljinski;
    }

    public void setDaljinski(boolean daljinski) {
        this.daljinski = daljinski;
    }

    public boolean isUkljucen() {
        return ukljucen;
    }

    public void setUkljucen(boolean ukljucen) {
        this.ukljucen = ukljucen;
    }

    public boolean isEnableOn() {
        return enableOn;
    }

    public void setEnableOn(boolean enableOn) {
        this.enableOn = enableOn;
    }

    public boolean isEnableOff() {
        return enableOff;
    }

    public void setEnableOff(boolean enableOff) {
        this.enableOff = enableOff;
    }
    
    
}
